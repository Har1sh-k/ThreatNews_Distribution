def get_emails():
    file_path="src/recipient_mails.txt"
    try:
        with open(file_path) as f:
            raw_mails = f.read()
        emails=raw_mails.split(";")
        return emails
    except Exception as e:
        status_logger.debug_logger('error',f"Unable to email file: {e}")
        return None

    
